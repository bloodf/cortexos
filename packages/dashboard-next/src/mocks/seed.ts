// Hash-chain seed
function fakeHash(prev: string, payload: string): string {
  let h = 5381;
  const s = `${prev}|${payload}`;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) >>> 0;
  return `${`00000000${h.toString(16)}`.slice(-8)}…`;
}
let chainPrev = "genesis…";
function ae(
  actor: string,
  tool: string,
  tool_class: string,
  decision: "allow" | "deny",
  reason: string,
  result: string,
  t: number,
): AuditEntry {
  const ts = iso(t);
  const id = `ae${ts}-${actor}`;
  const hash = fakeHash(chainPrev, `${actor}|${tool}|${decision}|${ts}`);
  chainPrev = hash;
  return {
    id,
    actor,
    tool,
    tool_class,
    args_hash: hash,
    decision,
    decision_reason: reason,
    result,
    created_at: ts,
  };
}
